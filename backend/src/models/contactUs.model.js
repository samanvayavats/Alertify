import mongoose from "mongoose";
const contactUsSchema = new mongoose.Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true

    }
})

export const ContactUs = mongoose.model("ContactUs" , contactUsSchema)